package modelos;

public class Pago {

    private long idPago;
    private String email;
    private long idPuja;

    public long getIdPago() {
        return idPago;
    }

    public void setIdPago(long idPago) {
        this.idPago = idPago;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getIdPuja() {
        return idPuja;
    }

    public void setIdPuja(long idPuja) {
        this.idPuja = idPuja;
    }
}